#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*new_node;

	new_node = malloc(sizeof(t_list));
	if (!new_node)
		return (NULL);
	new_node->content = content;
	new_node->next = NULL;
	return (new_node);
}

/* content parameter is assigned to the content field of the new node
new_node is a node
new_node->content
             ^field inside node;
new_node->next = NULL <- is to show that its the last node in the linked list
*/